/*
 * C++ program
 */
#include <iostream>
using namespace std;
int main(int argc, const char* argv[])
  {
     // Outputs 0, 1, 2, …, 9
     for (int Variable = 0; Variable <= 9; Variable++) {
        cout << Variable << endl;
     }
     // Outputs 9, 8, 7, …, 0
     for (int Variable = 9; Variable >= 0; Variable--) {
         cout << Variable << endl;
     }
  }

