public class Main {
  public static void main(String [] argv) {
   // Outputs 0, 1, 2, …, 9
   for (int Variable = 0; Variable <= 9; Variable++) {
      System.out.println (Variable);
   }
   // Outputs 9, 8, 7, …, 0
   for (int Variable = 9; Variable >= 0; Variable--) {
      System.out.println (Variable);
   }
  }
}

